package com.ty.pgms;

public class loops {
	public static void main(String[] args) {
		int a=1;
		while(a<=5)
		{
			System.out.println(a);
			a++;
		}
		for(int j=5;j>=0;j--)
		{
		for(int i=5;i>=j;i--)
		{
			System.out.print('*');
		}
		System.out.println();
	}
		int y=1;
		do {
			System.out.println(y);
			y++;
			
		}while(y<3);

		
	}
	
	
}
